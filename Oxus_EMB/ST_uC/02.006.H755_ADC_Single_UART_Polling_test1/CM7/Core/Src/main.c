  DebugPrint("UART Polling tested!\r\n");

  uint32_t adc_val = 0;
  float adc_vol = 0;
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {

    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
	  HAL_ADC_Start(&hadc1);								// Start ADC

	  if (HAL_ADC_PollForConversion(&hadc1, 10) == HAL_OK)  // Wait max 10 ms
		  {
	         	 adc_val = HAL_ADC_GetValue(&hadc1);
	         	 adc_vol = (3.3f * adc_val)/65535.0f;
	             DebugPrint("ADC1 Value: %lu| Voltage: %.2fV\r\n", adc_val,adc_vol);

	      }

	  HAL_ADC_Stop(&hadc1);                                 // Stop after one sample

	  HAL_Delay(500); // Sample every 0.5 s


	  uart_rx_polling();
  }
