void DebugPrint(const char *format, ...)
{
	// Wait for previous transmission to complete
	    while (uart_tx_busy);

	    va_list args;
	    va_start(args, format);
	    vsnprintf(uartBuffer, sizeof(uartBuffer), format, args);
	    va_end(args);

	    uart_tx_busy = true;

	    HAL_UART_Transmit_DMA(&huart2, (uint8_t *)uartBuffer, strlen(uartBuffer));
}

void check_dma_data(void)
{
    static uint32_t total_pos = 0;  // new: global linear counter

    uint16_t pos = sizeof(dma_rx_buffer) - __HAL_DMA_GET_COUNTER(&hdma_usart2_rx);

    if (pos != old_pos)
    {
        if (pos > old_pos)
        {
            // Normal forward move (no wrap)
            for (uint16_t i = old_pos; i < pos; i++)
            {
                DebugPrint("\r\nGot: '%c' (0x%02X) at pos %lu\r\n\r\n",
                           dma_rx_buffer[i], dma_rx_buffer[i], total_pos++);
            }
        }
        else
        {
            // Wrapped around â€” handle end part and restart from 0
            for (uint16_t i = old_pos; i < sizeof(dma_rx_buffer); i++)
            {
                DebugPrint("\r\nGot: '%c' (0x%02X) at pos %lu\r\n\r\n",
                           dma_rx_buffer[i], dma_rx_buffer[i], total_pos++);
            }
            for (uint16_t i = 0; i < pos; i++)
            {
                DebugPrint("\r\nGot: '%c' (0x%02X) at pos %lu\r\n\r\n",
                           dma_rx_buffer[i], dma_rx_buffer[i], total_pos++);
            }
        }

        old_pos = pos;
    }
}


uint16_t sensor_read_mcp3208(void)
{
    uint8_t addr = (0b00011000 | (0b010 & 0b111));  // channel = 2

    spi_tx_buf[0] = addr;   // Command byte
    spi_tx_buf[1] = 0;      // Dummy
    spi_tx_buf[2] = 0;      // Dummy

    spi_dma_done = 0;

    // CS LOW
    HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4, GPIO_PIN_RESET);

    // Start DMA transfer of 3 bytes
    HAL_SPI_TransmitReceive_DMA(&hspi1, spi_tx_buf, spi_rx_buf, 3);

    // Wait for DMA complete (still non-blocking for CPU)
    while (!spi_dma_done) { }

    // CS HIGH
    HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4, GPIO_PIN_SET);

    // Parse MCP3208 12-bit result:
    // rx_buf[1] contains high 6 bits
    // rx_buf[2] contains low 8 bits
    uint16_t adc = ((spi_rx_buf[1] & 0x3F) << 6) |
                   ((spi_rx_buf[2] & 0xFC) >> 2);

    return adc;
}


